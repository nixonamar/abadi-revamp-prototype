function initials(name){
  return name.replace(/,.*$/,'').split(/\s+/).filter(w=>/^[A-Za-z]/.test(w)).slice(0,2).map(w=>w[0]).join('').toUpperCase();
}

function PeopleGrid({ people }){
  return (
    <div className="people-grid">
      {people.map(p=>(
        <div className="person" key={p.nm}>
          <div className="pic"><span className="mono">{initials(p.nm)}</span></div>
          <div className="pinfo"><h3>{p.nm}</h3><div className="role">{p.role}</div></div>
        </div>
      ))}
    </div>);
}

